package fhv.eclipse2013.wwe.impl.scope;

public class ScopeSize {

	private int width;
	private int height;

	public ScopeSize(int w, int h) {
		this.width = w;
		this.height = h;
	}

	public int getWidth() {
		return this.width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return this.height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

}
